# Concatenate two strings: "Hello" and "World".
concatenated_string = "Hello" + " " + "World"
print(f"\nConcatenated string: {concatenated_string}")



# Find the length of the string "Python".
length_of_string = len("Python")
print(f"\nLength of 'Python': {length_of_string}")


# Convert the string "python" to uppercase.
uppercase_string = "python".upper()
print(f"\nUppercase of 'python': {uppercase_string}")


# Convert the string "HELLO" to lowercase.
lowercase_string = "HELLO".lower()
print(f"\nLowercase of 'HELLO': {lowercase_string}")


# Capitalize the first letter of the string "python".
capitalized_string = "python".capitalize()
print(f"\nCapitalized string: {capitalized_string}")


# Replace "world" with "there" in the string "Hello, world!".
replaced_string = "Hello, world!".replace("world", "there")
print(f"\nReplaced string: {replaced_string}")


# Split the string "apple,banana,cherry" into a list of fruits.
fruits_list = "apple,banana,cherry".split(",")
print(f"\nList of fruits: {fruits_list}")


# Count the number of times the letter "a" appears in the string "banana".
count_of_a = "banana".count("a")
print(f"\nNumber of 'a' in 'banana': {count_of_a}")


# Remove leading and trailing spaces from the string "  Hello, Python!  ".
trimmed_string = "  Hello, Python!  ".strip()
print(f"\nTrimmed string: '{trimmed_string}'")


# Join the list of words ["I", "love", "Python"] into a single string with spaces.
joined_string = " ".join(["I", "love", "Python"])
print(f"\nJoined string: {joined_string}")


